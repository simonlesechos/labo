        <h1 class="bigh1">Professionnels : votre obligation d’informer les consommateurs est renforcée !</h1>        
              <img src="../img/basedoc1.jpeg">
          <p><strong>Avant toute signature d’un contrat sur les lieux de vente, les professionnels doivent délivrer aux consommateurs un certain nombre d’informations qui viennent d’être listées par un décret.</strong></p>  
          <p>La loi relative à la consommation du 17 mars 2014 a imposé aux professionnels de communiquer aux consommateurs, avant toute conclusion d'un contrat de vente de biens ou de fourniture de services, des informations relatives notamment à leur identité, à leurs coordonnées postales, téléphoniques et électroniques et à leurs activités. Un récent décret est venu préciser la liste et le contenu de ces informations.

<small>Précision : en cas de manquement à cette obligation d'information, le professionnel encourra une amende administrative d'au plus 3 000 euros (15 000 euros pour une personne morale).</small></p>   
<h3>Informations à délivrer par tout professionnel</h3>
<p>Ainsi, le professionnel doit communiquer au consommateur :
<br>- son nom ou sa dénomination sociale, l'adresse géographique de son établissement et, si elle est différente, celle du siège social, son numéro de téléphone et son adresse électronique ; 
<br>- les modalités de paiement, de livraison et d'exécution du contrat ainsi que les modalités qu'il a prévues pour le traitement des réclamations ; 
<br>- s'il y a lieu, l'existence et les modalités d'exercice de la garantie légale de conformité et de la garantie des vices cachés ainsi que, le cas échéant, de la garantie commerciale et du service après-vente ;
<br>- s'il y a lieu, la durée du contrat ou, s'il s'agit d'un contrat à durée indéterminée ou à tacite reconduction, les conditions de sa résiliation ; 
<br>- s'il y a lieu, toute interopérabilité pertinente du contenu numérique avec certains matériels ou logiciels dont le professionnel a ou devrait raisonnablement avoir connaissance ainsi que les fonctionnalités du contenu numérique, y compris les mesures de protection technique applicables.</p>              
   <h3>Informations à délivrer par tout professionnel prestataire de services</h3>
   <p>Par ailleurs, avant la conclusion d'un contrat de fourniture de services et, lorsqu'il n'y a pas de contrat écrit, avant l'exécution de la prestation de services, tout professionnel doit communiquer au consommateur ou mettre à sa disposition les informations suivantes :
<br>- le statut et la forme juridique de l'entreprise ;
<br>- les coordonnées permettant d'entrer en contact rapidement et de communiquer directement avec lui ; 
<br>- le cas échéant, le numéro d'inscription au registre du commerce et des sociétés ou au répertoire des métiers ;
<br>- si son activité est soumise à un régime d'autorisation, le nom et l'adresse de l'autorité ayant délivré l'autorisation ;
<br>- s'il est assujetti à la TVA et identifié par un numéro individuel, son numéro individuel d'identification ;
<br>- s'il est membre d'une profession réglementée, son titre professionnel, l'État membre dans lequel il a été octroyé ainsi que, le cas échéant, le nom de l'ordre ou de l'organisme professionnel auprès duquel il est inscrit ;
<br>- les conditions générales, s'il en utilise ;
<br>- le cas échéant, les clauses contractuelles relatives à la législation applicable et la juridiction compétente ;
<br>- l'éventuelle garantie financière ou assurance de responsabilité professionnelle souscrite par lui, les coordonnées de l'assureur ou du garant ainsi que la couverture géographique du contrat ou de l'engagement.

Et, cette fois, à la demande du consommateur, le professionnel prestataire de services doit lui communiquer :
<br>- lorsque le prix n'est pas déterminé au préalable par le prestataire pour un type de service donné, le prix du service ou, lorsqu'un prix exact ne peut pas être indiqué, la méthode de calcul permettant au destinataire de vérifier ce dernier, ou un devis suffisamment détaillé ;
<br>- en ce qui concerne les professions réglementées, une référence aux règles professionnelles applicables dans l'État membre de l'Union européenne sur le territoire duquel ce professionnel est établi et aux moyens d'y avoir accès ;
<br>- des informations sur ses activités pluridisciplinaires et ses partenariats qui sont directement liés au service concerné et sur les mesures prises pour éviter les conflits d'intérêts. Ces informations figurent dans tout document d'information dans lequel le prestataire présente de manière détaillée ses services;
<br>- les éventuels codes de conduite auxquels il est soumis, l'adresse électronique à laquelle ces codes peuvent être consultés ainsi que les versions linguistiques disponibles ;
<br>- les informations sur les conditions de recours à des moyens extrajudiciaires de règlement des litiges, lorsque ces moyens sont prévus par un code de conduite, un organisme professionnel ou toute autre instance.</p>
