                                      <div class="article-content">
                                      <strong>La récente loi « Duflot » encadre les frais demandés par les agences
immobilières et les syndics de copropriété.</strong>
                                      <p>La loi pour l’accès au logement et un urbanisme rénové (loi Alur) a pour objectif, entre autres,
                                      de réduire et de rééquilibrer les coûts liés au logement supportés par les propriétaires et les
                                      locataires, et ce en encadrant les tarifs pratiqués par les agences immobilières et par les syndics
                                      de copropriété.</p>
                                      <h6>Les frais d’agence</h6>
                                      <p>La loi Alur impose pour les baux (location nue ou meublée) conclus depuis le 27 mars 2014,
                                      une répartition différente des frais d’agence entre le bailleur et le locataire. Jusqu’alors, les
                                      honoraires étaient partagés pour moitié entre les parties. Aujourd’hui, l’ensemble des coûts liés
                                      à la mise en location du bien sont à la seule charge du bailleur, à l’exception toutefois de quatre
                                      prestations qui sont partagées équitablement :
                                      - l’organisation de la visite,
                                      - la constitution du dossier du locataire,
                                      - la rédaction du bail,
                                      - la réalisation de l’état des lieux.
                                      Précision : le locataire est donc redevable de la moitié de ces frais. Mais pour éviter d’éventuelles
                                      dérives, ces frais seront plafonnés par décret en fonction de la surface habitable et de la
                                      localisation du bien loué.</p>
                                      <h6>Les tarifs des syndics</h6>
                                      <p>La loi Alur réforme également les tarifs appliqués par les syndics aux copropriétés.
                                        Généralement, ces derniers pratiquent deux types de facturation : les frais inhérents à la gestion
                                        courante d’un immeuble, comme par exemple la tenue d’une assemblée générale, et les frais dits
                                        particuliers (prestations supplémentaires), comme par exemple la gestion et le suivi de travaux.
                                        Ces frais étant tarifés de manière différente : les premiers sous la forme d’un forfait annuel et
                                        les seconds facturés à l’unité.<br>
                                        Afin d’éviter ce type d’abus, la loi prévoit qu’une liste des frais particuliers sera établie (par
                                        décret), les autres prestations étant donc nécessairement considérées comme des prestations de
                                        gestion courante.</p>
<br><em>Loi n° 2014-366 du 24 mars 2014, JO du 26</em>
