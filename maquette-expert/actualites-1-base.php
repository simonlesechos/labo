                                      <div class="article-content">
                                        <p>La loi relative à la consommation a modifié certaines dispositions relatives à la livraison d’un bien ou à l’exécution d’une prestation.</p>
                                        <p><i>Publié le 9 avril 2014</i></p>
                                        <p>Actuellement, le professionnel doit indiquer au client la date limite de livraison du bien (ou d'exécution de la prestation), lorsque celle-ci n'est pas immédiate, pour les contrats d'un montant supérieur à 500 €. En cas de dépassement de la date de livraison du bien ou d'exécution de la prestation excédant 7 jours et non dû à un cas de force majeure, le consommateur peut dénoncer le contrat par lettre recommandée avec demande d'avis de réception.</p>
                                        <p>À compter du 14 juin 2014, il devra, avant de conclure un contrat et quel qu'en soit son montant, indiquer au client la date limite de livraison du bien (ou d'exécution de la prestation).<br />
                                        À défaut d'indication ou d'accord sur la date, le professionnel devra livrer le bien ou exécuter la prestation sans retard injustifié et au plus tard 30 jours après la conclusion du contrat.<br />
                                        En cas de non-respect de la date prévue, ou du délai légal, le consommateur pourra annuler le contrat, par lettre recommandée avec demande d'avis de réception (ou par un écrit sur un autre support durable), si, après avoir enjoint, selon les mêmes modalités, le professionnel d'effectuer la livraison ou de fournir le service dans un délai supplémentaire raisonnable, ce dernier ne s'est pas exécuté dans ce délai. Le non-respect du délai de livraison, à lui seul, ne permettra donc plus au consommateur d'annuler le contrat. Ce dernier devra d'abord relancer le professionnel.</p>
                                        <p><i>Précision :<br />
                                          le consommateur pourra toutefois dénoncer immédiatement le contrat lorsque le professionnel refusera de livrer le bien ou de fournir le service ou lorsqu'il n'exécutera pas son obligation de livraison du bien ou de fourniture du service à la date ou à l'expiration du délai prévu et que cette date ou ce délai constituera pour le consommateur une condition essentielle du contrat.</i></p>
                                          <p>En cas d'annulation du contrat, le professionnel devra rembourser le consommateur de la totalité des sommes versées dans les 14 jours, au plus tard, suivant la date à laquelle le contrat aura été dénoncé. La somme versée au consommateur sera de plein droit majorée de 10 % si le remboursement intervient au plus tard 30 jours au-delà de ce terme, de 20 % jusqu'à 60 jours et de 50 % ultérieurement.</p>
                                          <p>Art. 6 I 2°, loi n° 2014-344 du 17 mars 2014, JO du 18</p>
